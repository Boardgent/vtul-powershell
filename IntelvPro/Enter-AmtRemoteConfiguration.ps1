Function Enter-AmtRemoteConfiguration  {
<#
  .Synopsis 
    Enters a Remote Configuration Session with an Intel Active Management Technology client
  .Description
    The Enter-AmtRemoteConfiguration Cmdlet starts an interactive configuration session with an unconfigured Intel Active Management Technology (AMT) client with firmware version 3.2 and higher.
      
    A PSRemoting session is required in order to discover information about the device and start the configuration service. A remote wsman session is then established with the device and remains the active session until Stop-AMTConfiguration is called.

 
  .Notes
    AMT Provisioning:
    AMT must in an unconfigured or remoteStarted state.
    HECI Drivers must be installed and working on the target AMT system.
    AMT must be enabed in the BIOS.
    Configuration Sessions are not supported over wireless connections.
    The Intel onboard Wired LAN must be connected.
    This command can only be used remotely.

    $ConfigurationCertificate shuold be set to the thumbprint of the desired certifcate in the directory Microsoft.PowerShell.Security\Certificate::CurrentUser\my
 
    Status:
      Status output designates if the Cmdlet was run successfully. For failed attempts additional status may be provided.
  .Link
    http:\\vproexpert.com
    http:\\www.intel.com\vpro
    http:\\www.intel.com

  .Example
    Enter-AmtRemoteConfiguration vprocomputer.vprodemo.com $ConfigurationCertificate
    
#>
[CmdletBinding()]
Param (

	[Parameter(Mandatory=$true,ValueFromPipelineByPropertyName=$true,ValueFromPipeline=$false, position=0, ParameterSetName="p1", HelpMessage="PSRemoting Session")]  [System.Management.Automation.Runspaces.PSSession]$Session,
    [Parameter(Mandatory=$true,ValueFromPipelineByPropertyName=$true,ValueFromPipeline=$false, position=0, ParameterSetName="p2", HelpMessage="PSRemoting ComputerName")]  [string]$ComputerName,
    [Parameter(Mandatory=$true,ValueFromPipelineByPropertyName=$true,ValueFromPipeline=$false, position=1, HelpMessage="AMT Setup Certificate")] [System.Security.Cryptography.X509Certificates.X509Certificate] $Certificate,
    [Parameter(Mandatory=$false,ValueFromPipelineByPropertyName=$true,ValueFromPipeline=$false, position=2,ParameterSetName="p2", HelpMessage="PS Credential")] [System.Management.Automation.PSCredential] $Credential,
    [Parameter(Mandatory=$false,ValueFromPipelineByPropertyName=$true,ValueFromPipeline=$false, position=3, HelpMessage="One time Password to use")] [string] $otp,
    [Parameter(Mandatory=$false, HelpMessage="Force session even if OTP does not match")] [Switch] $Force

) 



 PROCESS 
 {

   
    if ($session -eq $null)
    {
       if ($credential -eq $null)
       {
            $session = New-PSSession $ComputerName 
       }
       else
       {
            $Session = New-PSSession $ComputerName -Credential $credential
       }
       $CloseSession = $Session
      
    }
    
    if ($session -eq $null)
    {
        Write-Error "Unable to establish Remote PowerShell session with the remote client $ComputerName."
        Write-Error "Make sure PowerShell Remoting is enabled on remote client $ComputerName."
        Write-Error "See about_remote for information on running scripts on remote computers."
        return
    }
    
    
    
    if ($AmtIpVersion -eq $null)
    {
        $AmtIpVersion=4
    }
    
    
    #OTP is only used once then thown away so we will just use plain text
    if ([string]::IsNullOrEmpty($otp))
    {
        $otp = New-AmtPassword -AsPlainText -Force
    }


    #load the powershell module on the remote session
    Invoke-Command -Session $Session -ScriptBlock  { Import-Module IntelvPro }

    #next get setup information. This will contain Host and Networking information
    $SetupInfo = Invoke-Command -Session $session -ScriptBlock { Get-AmtSetup }
    
     #Start Remote configration
    if ($SetupInfo -ne $null)
    {
        $HostName = $SetupInfo.HostName
        $DomainName = $SetupInfo.DNSDomain
        
        $CfgStatus= $SetupInfo.SetupStatus
        $CfgVerion= $SetupInfo.CoreVersion.ToString()
        $CfgEnabled= $SetupInfo.RemoteConfigurationSupported
        $CfgIp= $SetupInfo.IPAddress
        $UUID= $SetupInfo.UUID
        
        Write-Verbose "ME Version $CfgVersion"
        Write-Verbose "System UUID=$UUID"
        Write-Verbose "SystemHost=$HostName.$DomainName"
        Write-Verbose "Configuation IP=$CfgIP"
        
        
        if (!$SetupInfo.RemoteConfigurationSupported)
        {
            Write-Warning "Remote configuration is not enabled in the ME BIOS"
        }
        
        if ($SetupInfo.SetupStatus -ne 1 -and $SetupInfo.SetupStatus -eq 0)
        {
            Write-Warning "ME is not in a configurable state"
        }
        
        if ($SetupInfo.IPAddress -eq $null -or $SetupInfo.IPAddress.Count -eq 0)
        {
            Write-Warning "Wired network connection not found.   ME device requires wired connection for configuration."
            $Service = "https://$HostName.$DomainName:16993/wsman"
        }
        else
        {
            $Service = $Service=Get-AmtUri -AddressList $setupInfo.IPAddress -Tls -ProtocolVersion $AmtIpVersion
        }
        
        
        if ([string]::IsNullOrEmpty($SetupInfo.DNSDomain))
        {
           Write-Warning "A DHCP reported DNSDomain is required for Remote Configuration"
        }
        
        if ($Certificate.Subject.IndexOf($SetupInfo.DNSDomain) -lt 0)
        {
           Write-Warning "Configuration Setup certificate does not contain DNSDomain $DomainName "
        }
        
           
        $hashFound = $false
        $RootThumbprint = Get-CertificateChain $Certificate -RootOnly 
        Foreach ($hash in $SetupInfo.ActiveHashes)
        {
            if ([System.String]::Compare($hash,$RootThumbprint.Thumbprint,$true) -eq 0)
            {
                $HashFound=$true
            }
        }
        if ($HashFound -eq $false)
        {
            Write-Warning "Root of configuration certificate not found in firmware $RootThumbprint.Thumbprint"
        }
        
        Invoke-Command -Session $session -ScriptBlock {param($otp,$AmtIpVersion) Start-AmtConfiguration -otp $otp -ProtocolVersion $AmtIpVersion } -Argumentlist $otp,$AmtIpVersion
        if ($CloseSession -ne $null) { Remove-PSSession $CloseSession }
    }
    else
    {
        Write-Error "Unable to obtain Setup Information from Remote Client.  Make sure the IntelvPro Module and ME drivers are installed on the remote client."
        return
    }
  
    
    $wsmanConnectionObject = new-object 'Intel.Management.Wsman.WsmanConnection' 
    $wsmanConnectionObject.Address=$Service
    $wsmanConnectionObject.Username = "admin" 
    $wsmanConnectionObject.Password = "admin"
    $wsmanConnectionObject.Options.ClientCertificate=$Certificate
    $wsmanConnectionObject.Options.HidePassword=$true

    Write-Verbose "Connecting to Wsman Service at $Service"


    $refToService = $wsmanConnectionObject.NewReference("SELECT * FROM AMT_SetupAndConfigurationService")
    $serviceObj = $refToService.Get()
    
    if ($serviceObj -eq $null)
    {
        Write-Verbose "Failed to establish configuration session"
        return
    }


    $deviceOtp= [System.Text.Encoding]::ASCII.GetString( [System.Convert]::FromBase64String($serviceObj.GetProperty("ProvisioningServerOTP").ToString()))
    if ($otp -eq $deviceOtp -or $Force.IsPresent)
    {
        if ($otp -ne $deviceOtp)
        {
            Write-Warning "One time passwords did not match. Using forced connection"
        }



        Write-Verbose "Extending configuration Period for 6 hours"
        $inputObj=$refToService.CreateMethodInput("ExtendProvisioningPeriod")
        $inputObj.SetProperty("Duration","6")
        $outObj=$refToService.InvokeMethod($inputObj)  
        
        Enter-AmtSession $wsmanConnectionObject
        
        Write-Verbose "Amt Session Started"
     
        Write-Verbose "Synchronizing the ME clock to Console time"
        Set-AmtClock
        
        Write-Verbose "Setting the ME Network Host information to $HostName.$DomainName"
        Set-AmtHost -Name $SetupInfo.HostName -Domain $SetupInfo.DNSDomain
      
    }
    else
    {
        Write-Error "Failed to verify AMT One time Password (Halting Configuration)"
        Write-Verbose "ConsoleOtp=$otp"
        Write-Verbose "DeviceOtp= $connetionOtp"
        
        $inputObj=$refToService.CreateMethodInput("ExtendProvisioningPeriod")
        $inputObj.SetProperty("Duration","0")
        $outObj=$refToService.InvokeMethod($inputObj)
    }

 } #end PROCESS

}# End Function Enter-AmtRemoteConfiguration

